const readline = require("readline");
const stdin = process.stdin;
readline.emitKeypressEvents(stdin);
stdin.setRawMode(true);
stdin.on("keypress", (c, key) => {
    if (key.name == "tab") {
        return;
    }
    
    if (key.name == "up") {
        return;
    }
    
    if (key.name == "down") {
        return;
    }
    
    if (key.name == "left") {
        return;
    }

    if (key.name == "right") {
        return;
    }
    
    if (key.name == 'c' && key.ctrl) {
        process.exit();
        return;
    }

    console.log(c, key)
});